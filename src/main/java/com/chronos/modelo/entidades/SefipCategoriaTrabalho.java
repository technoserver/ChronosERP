package com.chronos.modelo.entidades;

import javax.persistence.*;
import java.io.Serializable;


/**
 *
 * @author usuario
 */
@Entity
@Table(name = "sefip_categoria_trabalho")
public class SefipCategoriaTrabalho  implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id", nullable = false)
    private Integer id;
    @Column(name = "codigo")
    private Integer codigo;
    @Column(name = "nome", length = 2147483647)
    private String nome;

    public SefipCategoriaTrabalho() {
    }

    public SefipCategoriaTrabalho(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getCodigo() {
        return codigo;
    }

    public void setCodigo(Integer codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SefipCategoriaTrabalho)) {
            return false;
        }
        SefipCategoriaTrabalho other = (SefipCategoriaTrabalho) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return nome;
    }
    
}
